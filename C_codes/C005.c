
//____Data-Types_____Premitive

#include<stdio.h>

int main()

{

    int iNo1 = 10;            // 4 byte

    float fValue = 3.14;      // 4 byte

    double dData = 6.10;      // 8 byte

    char cValue = 'M';        // 1 byte..............->For char single Quoted is used ''

    printf("Integer is %d\n",iNo1);       // %d
    printf("Float is %f\n",fValue);       // %f
    printf("Double is %lf\n",dData);      // %lf  (ling float)
    printf("Char is  %c\n",cValue);       // %c   
                                          // %s   (String)
    return 0;
}

